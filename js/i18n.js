function updateLanguage(lang) {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(element => {
        const keys = element.getAttribute('data-i18n').split('.');
        let value = translations[lang];
        
        for (const key of keys) {
            if (value) {
                value = value[key];
            }
        }

        if (value) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = value;
            } else {
                element.textContent = value;
            }
        }
    });
}

// Initialize with default language
document.addEventListener('DOMContentLoaded', () => {
    const userLang = navigator.language.split('-')[0];
    const supportedLangs = Object.keys(translations);
    const defaultLang = supportedLangs.includes(userLang) ? userLang : 'en';
    
    document.documentElement.lang = defaultLang;
    document.querySelector('.current-lang').textContent = defaultLang.toUpperCase();
    
    const langLink = document.querySelector(`[data-lang="${defaultLang}"]`);
    if (langLink) {
        document.querySelectorAll('.language-dropdown a').forEach(a => a.classList.remove('active'));
        langLink.classList.add('active');
    }
    
    updateLanguage(defaultLang);
}); 